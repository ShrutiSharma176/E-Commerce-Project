package Dto;


public class Supplier {
    String supp_id,supp_name,password,supp_email,address,company;
    String supp_phone,status;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getSupp_id() {
        return supp_id;
    }

    public void setSupp_id(String supp_id) {
        this.supp_id = supp_id;
    }

    public String getSupp_name() {
        return supp_name;
    }

    public void setSupp_name(String supp_name) {
        this.supp_name = supp_name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSupp_email() {
        return supp_email;
    }

    public void setSupp_email(String supp_email) {
        this.supp_email = supp_email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getSupp_phone() {
        return supp_phone;
    }

    public void setSupp_phone(String supp_phone) {
        this.supp_phone = supp_phone;
    }

    
}
